                        <center></a><h2 class="post-title"><a href="#"> Nuestra Historia</a></center>
                            <a href="#" class="thumb pull-right"><img src="<?php echo base_url(); ?>/estilos/imagenes/inicio.jpg" alt="" /></a> 
            
                                    <p class="text-justify">El Instituto Autónomo de Puertos Públicos del Estado Falcón (I.A.P.P.P.E.F.), bajo la Gestión del Tcnel. (GNB) Alexis Donquiz, ajustado a los lineamientos dictados por la Gobernadora Stella Lugo de Montilla, ha experimentado cambios de forma innovadora y efectiva; referente a tecnología, seguridad y una buena administración, muestra de ello es la certificación recibida por el Puerto Internacional de Guaranao, por parte del Instituto Nacional de Espacios Acuáticos (INEA) y la Organización Marítima Internacional (OMI), que lo avala como Puerto Seguro internacional para los próximos 5 años, obtenida en el primer semestre del año 2012.
Desde sus inicios en el año 1951, en el Puerto Internacional de Guaranao comienza la construcción de un muelle de concreto o muelle fijo, el cual sirvió para realizar operaciones de carga y descarga de buques de hasta 100 metros de eslora, además de servir para el atraque de embarcaciones pesqueras.
Debido al incremento del volumen de carga y descarga, también por la llegada de buques de gran calado y eslora, nace la necesidad de instalar un muelle acondicionado a las exigencias de la región.
Es así como en el año 1978, fue instalado el muelle II flotante, quedando este para el atraque y operaciones de buques de hasta 180 metros de eslora y 10 metros de calado respectivamente. El muelle I, paso a ser utilizado para atracar embarcaciones menores (lanchas de pilotaje, lanchas con mercancías hacia las Islas de Aruba y las Antillas).
A través de su historia el Puerto Internacional de Guaranao, ha sido administrado por varias figuras organizativas, siendo la primera “Organización de Servicios Portuarios” (O.S.P), adscrito al Ministerio de Hacienda, luego el Instituto Nacional de Puertos (I.N.P), adscrito al Ministerio de Transporte y Comunicaciones (M.T.C)
Para el año 1991, pasa a formar parte de los puertos descentralizados, es así como el 15 de Enero de 1992, el Puerto es transferido al Gobierno Regional, estructurándose por un régimen transitorio, que luego pasaría a ser Dirección de Puertos del Estado Falcón. Hasta el año 1995, cuando se crea el Instituto de Puertos Públicos del Estado Falcón, (I.A.P.P.E.P), según Gaceta Oficial de fecha 29 de Mayo de 1995.
